"""Listas en Python"""
"""Listas (reverse):revierte el orden de los elementos de una lista"""

datos_empleados= ["Susana", "Dominguez", 39, "20/04/1984"]

print("Los datos de mi lista es: {}".format(datos_empleados))

datos_empleados.reverse()
print("Los valores de mi nueva lista son: {}".format(datos_empleados))

print(datos_empleados)
