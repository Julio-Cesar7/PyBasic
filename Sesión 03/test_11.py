"""Listas en Python"""
"""Listas (copy):copia todos los valores de una lista a otra variable"""

lis_1 = ["Susana", "Dominguez", 39, "99998888"]
lis_2= lis_1.copy()
lis_2.append("San Miguel")

print("El contenido de mi lista 1 es: {}".format(lis_1))
print("El contenido de mi lista 2 es: {}".format(lis_2))



