"""Listas en Python"""
"""Listas (del):elimina un valor de la lista enviando una posición existente """

lis_1=[0, 2, 4, 6, 16, 20, 26]
del lis_1[2]
print("Mi lista actualizada tiene los sig. valores: {}".format(lis_1))

"""Eliminando el último valor de la lista"""
del lis_1[-1] #-1: ultimo valor de la lista
print("Mi lista actualizada tiene los sig. valores: {}".format(lis_1))

